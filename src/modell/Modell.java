package modell;


public class Modell {
    private int kor;
    private String nev;
    private String tanult;
    

    public Modell(int kor, String nev, String tanult) {
        this.kor=kor;
        this.nev=nev;
        this.tanult=tanult;
    }

    public int getKor() {
        return kor;
    }

    public String getNev() {
        return nev;
    }

    public String getTanult() {
        return tanult;
    }
    
     
    
    
}
